static unsigned int standart_89_encr_test(void)
{
	unsigned int textLen = sizeof(kGost89StandartPlainText);
	unsigned int res = 0;

	unsigned char ctx[kEcb89ContextLen];
	unsigned char output[sizeof(kGost89StandartPlainText)];
	unsigned char outputE[sizeof(kGost89StandartPlainText)];

	//Шифрование
	if(!cypher_magma_init(kMasterKeyGost89, ctx))
		goto exit;
	if(!cypher_encr_ecb(ctx, kGost89StandartPlainText, output, textLen))
		goto exit;
	if(memcmp(output, kGost89StandartEncrText, textLen))
		goto exit;

	free_ecb(ctx);

	//Расшифрование
	if(!cypher_magma_init(kMasterKeyGost89, ctx))
		goto exit;
	if(!cypher_decr_ecb(ctx, output, outputE, textLen))
		goto exit;
	if(memcmp(outputE, kGost89StandartPlainText, textLen))
		goto exit;
	res = 1;
exit:
	free_ecb(ctx);
	return res;
}

