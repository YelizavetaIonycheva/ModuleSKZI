static unsigned int f(unsigned int n1)
{
  unsigned int n2,n3;
  
  n2=n1^0x3a7f17ec;
  n3=(f2_1[(n2>>28)&0xf])<<28;            
  n3=n3^(f2_2[(n2>>24)&0xf])<<24;
  n3=n3^(f2_3[(n2>>20)&0xf])<<20;
  n3=n3^(f2_4[(n2>>16)&0xf])<<16;
  n3=n3^(f2_5[(n2>>12)&0xf])<<12;
  n3=n3^(f2_6[(n2>>8)&0xf])<<8;
  n3=n3^(f2_7[(n2>>4)&0xf])<<4;
  n3=n3^(f2_8[n2&0xf]);
  
  return n3;
}

static inline unsigned int g(unsigned int n1)
{
  return (n1 * n1 * 108540) + (n1 * 400001) + 256431;
}

